CREATE TABLE [produto] ([id] integer PRIMARY KEY, [nome] nvarchar(255), [SKU] nvarchar(255) UNIQUE NOT NULL, [UPC] nvarchar(255) UNIQUE NOT NULL, [id_marca] integer NOT NULL, [id_subcategoria] integer NOT NULL, [preco_unitario] decimal(10,2) NOT NULL);
