CREATE TABLE [vinculo_profissional] ([id] integer PRIMARY KEY, [id_trabalhador] integer NOT NULL, [id_cargo] integer NOT NULL, [data_de_inicio] datetime2(0), [data_de_fim] datetime2(0), [ativo] bit);
GO
CREATE TABLE [concessionario] ([id] integer PRIMARY KEY, [nome] nvarchar(255), [telefone] nvarchar(20), [email] nvarchar(255), [morada] nvarchar(255), [codigo_postal] nvarchar(10), [id_freguesia] integer NOT NULL, [area_total] decimal(10,2), [id_responsavel] integer NOT NULL, [nif] nvarchar(20) NOT NULL UNIQUE);
GO
CREATE TABLE [armazem] ([id] integer PRIMARY KEY, [nome] varchar(225), [id_concessionario] integer NOT NULL, [area_total] decimal(10,2), [id_responsavel] integer NOT NULL);
GO
CREATE TABLE [oficina] ([id] integer PRIMARY KEY, [nome] nvarchar(255), [id_concessionario] integer NOT NULL, [area_total] decimal(10,2), [id_responsavel] integer NOT NULL);
GO
CREATE TABLE [entrada] ([id] integer PRIMARY KEY, [id_produto] integer NOT NULL, [id_trabalhador] integer NOT NULL, [id_fornecedor] integer NOT NULL, [id_armazem] integer NOT NULL, [data_hora] datetime2(0), [quantidade] integer, [identificador_documento] nvarchar(255));
GO
CREATE TABLE [saida] ([id] integer PRIMARY KEY, [id_produto] integer NOT NULL, [id_trabalhador] integer NOT NULL, [id_armazem] integer NOT NULL, [data_hora] datetime2(0), [tipo_saida] nvarchar(255), [quantidade] integer, [identificador_documento] nvarchar(255));
GO

